# Sequences: String, List, Tuple

# Indexing: access any item in the sequence using its index. It starts with 0 for the first element.

# Slicing: slice out substrings, sublists, subtuples using indexes
# [start : end+1 : step]

# string
x = 'frog'
print (x[3])

# list
x = ['pig', 'cow', 'horse']
print (x[1])

# tuple
x = ('Kevin', 'Niklas', 'Jenny', 'Craig')
print (x[0])

# Slicing

x = 'computer'
print(x[1:4])
print(x[1:6:2])
print(x[3:])
print(x[:5])
print(x[-1])
print(x[-3:])
print(x[:-2])
